"""Tests for the RemoteConfigPatchStrategy seam.

This file is the regression contract for the strategy refactor that abstracts
ccstack's Spring Cloud config-server disable + devstack-local.yml overlay flow
behind a Protocol-based seam.

Two responsibilities:

1. Capture BYTE-IDENTICAL golden outputs for the existing Gradle/Spring path
   (`write_managed_gradle_init(include_config_server_patch=True)` and
   `write_managed_local_yml`). These tests must pass both before and after the
   refactor — the public functions keep their existing behavior, and the new
   strategy class delegates to them with identical artifact bytes.

2. Specify the new DotenvStrategy contract for type=command workspaces:
   .env-style overlay file generation + env injection map.

The strategy registry / selection logic is also exercised here.
"""

from __future__ import annotations

import hashlib
import importlib.util
import sys
import tempfile
import unittest
from importlib.machinery import SourceFileLoader
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
# Pick the renamed devstack file when present (post git mv);
# fall back to ccstack for any in-progress checkout.
DEVSTACK_PATH = ROOT / "devstack" if (ROOT / "devstack").exists() else ROOT / "ccstack"
CCSTACK_PATH = DEVSTACK_PATH  # legacy alias for older tests


def _load_ccstack():
    loader = SourceFileLoader("ccstack_module_strategies", str(CCSTACK_PATH))
    spec = importlib.util.spec_from_loader(loader.name, loader)
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    loader.exec_module(module)
    return module


ccstack = _load_ccstack()


