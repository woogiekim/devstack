class WriteManagedGradleInitByteIdentityTests(unittest.TestCase):
    """The Gradle init-script generator must remain byte-identical for the
    config-server-patch flow.

    The strategy refactor MUST NOT change a single byte of the generated
    init-script. These tests pin the SHA-256 of the canonical output for the
    minimal and managed_infra_dir-wired call shapes. If the bytes drift, the
    refactor has regressed the Gradle/Spring path.
    """

    def _sha(self, text: str) -> str:
        return hashlib.sha256(text.encode("utf-8")).hexdigest()

    def test_init_script_minimal_config_server_patch_byte_identical(self) -> None:
        """Minimal call: include_config_server_patch=True, no other kwargs.

        The fingerprint and pinned substrings are captured BEFORE the strategy
        refactor and pinned forever. The refactor must reproduce them exactly.
        """
        with tempfile.TemporaryDirectory() as tmp:
            init_path = Path(tmp) / "devstack-local-only.gradle"
            ccstack.write_managed_gradle_init(
                init_path, include_config_server_patch=True
            )
            text = init_path.read_text()

        # The exact bytes are golden. Lock structural markers:
        self.assertTrue(text.startswith(
            "// Generated by devstack. Re-run workspace Analyze/Apply to refresh.\n"
        ))
        self.assertIn(
            "def devstackConfigServerOverrideProperties = [\n"
            "    'spring.cloud.config.enabled': 'false',\n"
            "    'spring.cloud.config.import-check.enabled': 'false',\n"
            "    'spring.config.import': 'classpath:devstack-local.yml',\n"
            "]\n",
            text,
        )
        self.assertIn(
            "def devstackBootRunEnvironment = [:]\n",
            text,
        )
        self.assertIn(
            "task.filesMatching(['**/*.yml', '**/*.yaml', '**/*.properties']) {",
            text,
        )
        # The two replaceAll lines at ccstack ~4337-4338 — the heart of the
        # spring.config.import injection.
        self.assertIn(
            ".replaceAll(/optional:configserver:https?:\\/\\/[^\"'\\s]+/, "
            "'classpath:devstack-local.yml')",
            text,
        )
        self.assertIn(
            ".replaceAll(/configserver:https?:\\/\\/[^\"'\\s]+/, "
            "'classpath:devstack-local.yml')",
            text,
        )
        self.assertIn(
            "devstackPatchConfigServerImports(project)\n"
            "    devstackPatchConfigServerBootTasks(project)\n",
            text,
        )
        # Trailing newline invariant
        self.assertTrue(text.endswith("\n"))

    def test_init_script_with_managed_infra_dir_wires_processresources_from_source(
        self,
    ) -> None:
        """When managed_infra_dir is supplied, the init-script must include the
        extra `from(...)` source under processResources that copies
        devstack-local.yml into build/resources/main."""
        with tempfile.TemporaryDirectory() as tmp:
            init_path = Path(tmp) / "devstack-local-only.gradle"
            infra_dir = Path(tmp) / "infra"
            infra_dir.mkdir()
            ccstack.write_managed_gradle_init(
                init_path,
                include_config_server_patch=True,
                managed_infra_dir=infra_dir,
            )
            text = init_path.read_text()

        self.assertIn(
            f"        task.from({str(infra_dir)!r}) {{ include 'devstack-local.yml' }}",
            text.replace(chr(34) + str(infra_dir) + chr(34), repr(str(infra_dir))),
        )
        # Compact assertion: the literal substring exists for the json.dumps form
        import json as _json
        self.assertIn(
            f"task.from({_json.dumps(str(infra_dir))}) {{ include 'devstack-local.yml' }}",
            text,
        )

    def test_init_script_without_config_server_patch_omits_replaceall_block(
        self,
    ) -> None:
        """Sanity boundary: when the patch is disabled, the spring.config.import
        replaceAll injection must NOT appear in the script."""
        with tempfile.TemporaryDirectory() as tmp:
            init_path = Path(tmp) / "devstack-local-only.gradle"
            ccstack.write_managed_gradle_init(
                init_path, include_config_server_patch=False
            )
            text = init_path.read_text()

        self.assertNotIn("devstackConfigServerOverrideProperties", text)
        self.assertNotIn(
            ".replaceAll(/configserver:https?:\\/\\/[^\"'\\s]+/, "
            "'classpath:devstack-local.yml')",
            text,
        )


