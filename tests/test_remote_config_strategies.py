class DotenvStrategyTests(unittest.TestCase):
    """The DotenvStrategy materializes a .env-style overlay file under
    managed_infra_dir and declares the env injection contract for command
    services.
    """

    def test_dotenv_file_written_for_command_workspace(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            infra = Path(tmp)
            services = {
                "alpha": {"type": "command", "command": ["python", "alpha.py"]},
            }
            ctx = _make_ctx("ws", services, infra)
            strategy = ccstack.DotenvStrategy()
            result = strategy.materialize(ctx)

            env_path = infra / "devstack-local.env"
            self.assertTrue(env_path.is_file(),
                            "DotenvStrategy must write devstack-local.env under managed_infra_dir")

    def test_dotenv_contains_expected_spring_keys(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            infra = Path(tmp)
            services = {"alpha": {"type": "command", "command": ["python", "alpha.py"]}}
            ctx = _make_ctx("ws", services, infra)
            strategy = ccstack.DotenvStrategy()
            strategy.materialize(ctx)

            text = (infra / "devstack-local.env").read_text()
            self.assertIn("SPRING_CLOUD_CONFIG_ENABLED=false", text)
            self.assertIn("SPRING_CLOUD_CONFIG_IMPORT_CHECK_ENABLED=false", text)
            self.assertIn("SPRING_CONFIG_IMPORT=classpath:devstack-local.yml", text)
            # Comment header for human readers
            self.assertTrue(
                text.startswith("# managed by devstack — do not edit."),
                f"dotenv must start with managed comment header; got: {text[:80]!r}",
            )

    def test_dotenv_env_injection_map_returned_for_command_services(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            infra = Path(tmp)
            services = {
                "alpha": {"type": "command"},
                "beta": {"type": "command"},
                "gamma_compose": {"type": "compose"},  # ignored
            }
            ctx = _make_ctx("ws", services, infra)
            strategy = ccstack.DotenvStrategy()
            artifacts = strategy.materialize(ctx)

            self.assertIn("env_injection", artifacts)
            env_injection = artifacts["env_injection"]
            # The injection map applies to command services only
            self.assertIn("alpha", env_injection)
            self.assertIn("beta", env_injection)
            self.assertNotIn("gamma_compose", env_injection)
            self.assertEqual(env_injection["alpha"]["SPRING_CONFIG_IMPORT"],
                             "classpath:devstack-local.yml")

    def test_no_dotenv_written_for_gradle_only_workspace(self) -> None:
        """DotenvStrategy.applies_to must return False when there are no command
        services; even if materialize is called directly, no .env should be
        emitted because the strategy is inapplicable."""
        with tempfile.TemporaryDirectory() as tmp:
            infra = Path(tmp)
            services = {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}}
            ctx = _make_ctx("ws", services, infra)
            strategy = ccstack.DotenvStrategy()
            self.assertFalse(strategy.applies_to(ctx),
                             "DotenvStrategy must not apply to gradle-only workspaces")

    def test_dotenv_disabled_when_requires_config_resource_patch_false(self) -> None:
        """If the workspace does not need the config-server patch, DotenvStrategy
        must not apply even when command services are present."""
        with tempfile.TemporaryDirectory() as tmp:
            infra = Path(tmp)
            services = {"alpha": {"type": "command"}}
            ctx = _make_ctx("ws", services, infra,
                            requires_config_resource_patch=False)
            strategy = ccstack.DotenvStrategy()
            self.assertFalse(strategy.applies_to(ctx))


