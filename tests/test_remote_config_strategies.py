class MaterializeManagedEnvironmentDelegationTests(unittest.TestCase):
    """After the refactor, materialize_managed_environment delegates the
    config-server patch flow to the strategy registry. This test ensures the
    delegation does not change observable behavior for a representative
    Gradle workspace."""

    def test_gradle_workspace_still_writes_init_and_local_yml(self) -> None:
        """End-to-end smoke: a workspace that exercises the
        requires_config_resource_patch path must still produce the
        devstack-local-only.gradle init-script and the devstack-local.yml file
        in the managed infra dir."""
        # This test is intentionally a smoke test — the deep byte-identity
        # contract is enforced by the section above. Here we just confirm the
        # strategy is wired in and produces the artifacts.
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp) / "ws"
            root.mkdir()
            # We don't run a full materialize_managed_environment here (it
            # has many other side effects); instead we exercise the strategy
            # selection through the public seam directly.
            infra = Path(tmp) / "infra"
            infra.mkdir()
            services = {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}}
            ctx = _make_ctx("ws", services, infra)
            for strategy in ccstack.select_remote_config_strategies(ctx):
                strategy.materialize(ctx)
            self.assertTrue((infra / "devstack-local.yml").is_file())
            self.assertTrue((infra / "devstack-local-only.gradle").is_file())


