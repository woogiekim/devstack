class StrategyRegistryTests(unittest.TestCase):
    """`select_remote_config_strategies(ctx)` returns the list of strategies
    that apply to the given workspace context."""

    def _names(self, strategies):
        return sorted(s.name for s in strategies)

    def test_select_returns_gradle_strategy_for_gradle_workspace(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            ctx = _make_ctx(
                "ws",
                {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}},
                Path(tmp),
            )
            selected = ccstack.select_remote_config_strategies(ctx)
            self.assertEqual(self._names(selected), ["gradle-spring"])

    def test_select_returns_dotenv_strategy_for_command_workspace(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            ctx = _make_ctx(
                "ws",
                {"alpha": {"type": "command"}},
                Path(tmp),
            )
            selected = ccstack.select_remote_config_strategies(ctx)
            self.assertEqual(self._names(selected), ["dotenv"])

    def test_select_returns_both_for_mixed_workspace(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            ctx = _make_ctx(
                "ws",
                {
                    "alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"},
                    "beta": {"type": "command"},
                },
                Path(tmp),
            )
            selected = ccstack.select_remote_config_strategies(ctx)
            self.assertEqual(self._names(selected), ["dotenv", "gradle-spring"])

    def test_select_returns_empty_when_no_config_server_requirement(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            ctx = _make_ctx(
                "ws",
                {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}},
                Path(tmp),
                requires_config_resource_patch=False,
            )
            selected = ccstack.select_remote_config_strategies(ctx)
            self.assertEqual(selected, [])

    def test_select_returns_empty_when_no_services(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            ctx = _make_ctx("ws", {}, Path(tmp))
            selected = ccstack.select_remote_config_strategies(ctx)
            self.assertEqual(selected, [])


