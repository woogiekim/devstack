_UNSET = object()


def _make_ctx(workspace_name: str, services: dict, infra_dir: Path, **kwargs):
    """Build a RemoteConfigPatchContext for tests.

    Pops the context-level kwargs (``requires_config_resource_patch``,
    ``gradle_init_path``, ``managed_infra_dir_for_patch``) and forwards the
    remainder into ``gradle_init_kwargs`` (which mirror
    ``write_managed_gradle_init``'s kwargs).
    """
    requires = kwargs.pop("requires_config_resource_patch", True)
    init_path = kwargs.pop(
        "gradle_init_path", infra_dir / "devstack-local-only.gradle"
    )
    infra_for_patch = kwargs.pop("managed_infra_dir_for_patch", _UNSET)

    ctx_kwargs = dict(
        workspace_name=workspace_name,
        managed_infra_dir=infra_dir,
        services=services,
        requires_config_resource_patch=requires,
        gradle_init_path=init_path,
        gradle_init_kwargs=kwargs,
    )
    if infra_for_patch is not _UNSET:
        ctx_kwargs["managed_infra_dir_for_patch"] = infra_for_patch
    return ccstack.RemoteConfigPatchContext(**ctx_kwargs)


class GradleSpringStrategyByteIdentityTests(unittest.TestCase):
    """The strategy must reproduce the existing functions' output bit-for-bit
    when materialized for an equivalent context.

    Comparison contract:
      golden = write_managed_local_yml + write_managed_gradle_init  (direct call)
      via_strategy = GradleSpringStrategy().materialize(ctx)        (seam call)
    The bytes of both devstack-local.yml and devstack-local-only.gradle must be
    identical. This is the v1 backward-compatibility regression boundary.
    """

    def test_strategy_produces_identical_local_yml_to_direct_write(self) -> None:
        with tempfile.TemporaryDirectory() as tmp_direct, tempfile.TemporaryDirectory() as tmp_strat:
            # Direct path
            direct_path = Path(tmp_direct) / "devstack-local.yml"
            ccstack.write_managed_local_yml(direct_path)
            direct_bytes = direct_path.read_bytes()

            # Strategy path
            strat_infra = Path(tmp_strat)
            services = {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}}
            ctx = _make_ctx("ws", services, strat_infra)
            strategy = ccstack.GradleSpringStrategy()
            strategy.materialize(ctx)
            strat_path = strat_infra / "devstack-local.yml"
            self.assertTrue(strat_path.is_file(),
                            "GradleSpringStrategy must materialize devstack-local.yml")
            strat_bytes = strat_path.read_bytes()

            self.assertEqual(direct_bytes, strat_bytes,
                             "Strategy output must be byte-identical to direct call")

    def test_strategy_produces_identical_init_script_to_direct_write(self) -> None:
        with tempfile.TemporaryDirectory() as tmp_direct, tempfile.TemporaryDirectory() as tmp_strat:
            # Direct path
            direct_init = Path(tmp_direct) / "devstack-local-only.gradle"
            direct_infra = Path(tmp_direct) / "infra"
            direct_infra.mkdir()
            ccstack.write_managed_gradle_init(
                direct_init,
                include_config_server_patch=True,
                managed_infra_dir=direct_infra,
            )
            direct_bytes = direct_init.read_bytes()

            # Strategy path
            strat_infra = Path(tmp_strat)
            services = {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}}
            ctx = _make_ctx(
                "ws",
                services,
                strat_infra,
                gradle_init_path=strat_infra / "devstack-local-only.gradle",
                managed_infra_dir_for_patch=strat_infra,
            )
            strategy = ccstack.GradleSpringStrategy()
            strategy.materialize(ctx)
            strat_init = strat_infra / "devstack-local-only.gradle"
            self.assertTrue(strat_init.is_file())
            strat_bytes = strat_init.read_bytes()

            # The infra_dir paths embedded in the init-script differ between
            # the two tmpdirs, so we cannot compare raw bytes here — instead
            # we strip the tmpdir prefixes and compare the canonicalized text.
            direct_text = direct_init.read_text().replace(str(direct_infra), "INFRA")
            strat_text = strat_init.read_text().replace(str(strat_infra), "INFRA")
            self.assertEqual(direct_text, strat_text,
                             "Init-script bytes must be identical after path canonicalization")

    def test_strategy_byte_identical_for_no_infra_dir_minimal_case(self) -> None:
        """When managed_infra_dir_for_patch is None, no `from(...)` extra source is
        emitted — matching the legacy unit-test call shape."""
        with tempfile.TemporaryDirectory() as tmp_direct, tempfile.TemporaryDirectory() as tmp_strat:
            direct_init = Path(tmp_direct) / "devstack-local-only.gradle"
            ccstack.write_managed_gradle_init(direct_init, include_config_server_patch=True)
            direct_bytes = direct_init.read_bytes()

            strat_infra = Path(tmp_strat)
            services = {"alpha": {"type": "gradle", "task": ":apps:alpha:bootRun"}}
            ctx = _make_ctx(
                "ws",
                services,
                strat_infra,
                gradle_init_path=strat_infra / "devstack-local-only.gradle",
                managed_infra_dir_for_patch=None,
            )
            strategy = ccstack.GradleSpringStrategy()
            strategy.materialize(ctx)
            strat_init = strat_infra / "devstack-local-only.gradle"
            self.assertEqual(direct_bytes, strat_init.read_bytes(),
                             "Without managed_infra_dir_for_patch, bytes must match exactly")


